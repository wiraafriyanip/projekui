    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("update tbbuku set judulbuku=?, penerbit=?, jumlah=? where idbuku=?");
            stmt.setString(1, tjudul.getText());
            stmt.setString(2, tpenerbit.getText());
            stmt.setString(3, tjumlah.getText());
            stmt.setString(4, tidbuku.getText());
            
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
            
            tjudul.setText("");
            tpenerbit.setText("");
            tjumlah.setText("");
            tidbuku.setText("");
            
             bBaru.setEnabled(true);
             bsimpan.setEnabled(true);
             bedit.setEnabled(true);
             bhapus.setEnabled(true);
             
        }catch (SQLException e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_beditActionPerformed

