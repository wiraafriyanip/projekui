public void TampilData() {
    DftTblModel_buku = new DefaultTableModel();
    DftTblModel_buku.addColumn("idbuku");
    DftTblModel_buku.addColumn("judulbuku");
    DftTblModel_buku.addColumn("penerbit");
    DftTblModel_buku.addColumn("jumlah");
    
    jtbbuku.setModel(DftTblModel_buku);
    Connection conn = koneksi.getConnection();
    try {
        java.sql.Statement stmt = conn.createStatement(); 
         SQL ="select * from tbbuku";
        java.sql.ResultSet res = stmt.executeQuery(SQL);
        while (res.next()) {
            DftTblModel_buku.addRow(new Object []{
                res.getString ("idbuku"),
                res.getString ("judulbuku"),
                res.getString ("penerbit"),
                res.getString ("jumlah"),
            });
        }
    } catch (SQLException e) {
        System.out.println(e.getMessage());
    }
}
