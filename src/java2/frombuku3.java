    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        try {
             Connection coon = koneksi.getConnection();
            PreparedStatement stmt = coon.prepareStatement("insert into tbbuku(judulbuku, penerbit, jumlah) values (?,?,?)");
            stmt.setString(1, tjudul.getText());
            stmt.setString(2, tpenerbit.getText());
            stmt.setString(3, tjumlah.getText());
            
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
            tjudul.setText("");
            tpenerbit.setText("");
            tjumlah.setText("");
            tidbuku.setText("");
            
        }catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_bsimpanActionPerformed

