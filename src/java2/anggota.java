public void TampilData() {
    DftTblModel_anggota = new DefaultTableModel();
    DftTblModel_anggota.addColumn("ID ANGGOTA");
    DftTblModel_anggota.addColumn("NAMA ANGGOTA");
    DftTblModel_anggota.addColumn("KELAS");
    DftTblModel_anggota.addColumn("JURUSAN");
    
    jtbanggota.setModel(DftTblModel_anggota);
    Connection conn = koneksi.getConnection();
    try {
        java.sql.Statement stmt = conn.createStatement(); 
         SQL ="select * from tbanggota1";
        java.sql.ResultSet res = stmt.executeQuery(SQL);
        while (res.next()) {
            DftTblModel_anggota.addRow(new Object []{
                res.getString ("nomor_anggota"),
                res.getString ("nama"),
                res.getString ("kelas"),
                res.getString ("jurusan"),
            });
        }
    } catch (SQLException e) {
        System.out.println(e.getMessage());
    }}
