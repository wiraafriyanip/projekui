public void TampilData() {
    DftTblModel_petugas = new DefaultTableModel();
    DftTblModel_petugas.addColumn("ID PETUGAS");
    DftTblModel_petugas.addColumn("NAMA PETUGAS");
    DftTblModel_petugas.addColumn("USERNAME");
    DftTblModel_petugas.addColumn("PASSWORD");
    
    jtbpetugas.setModel(DftTblModel_petugas);
    Connection conn = koneksi.getConnection();
    try {
        java.sql.Statement stmt = conn.createStatement(); 
         SQL ="select * from tbpetugas";
        java.sql.ResultSet res = stmt.executeQuery(SQL);
        while (res.next()) {
  
            DftTblModel_petugas.addRow(new Object []{
                res.getString ("idpetugas"),
                res.getString ("nama"),
                res.getString ("username"),
                res.getString ("password"),
            });
        }
    } catch (SQLException e) {
        System.out.println(e.getMessage());
    }}
