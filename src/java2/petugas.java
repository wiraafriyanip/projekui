    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        try {
            Connection coon = koneksi.getConnection();
            PreparedStatement stmt = coon.prepareStatement("insert into tbpetugas(nama, username, password) values (?,?,?)");
            stmt.setString(1, nama.getText());
            stmt.setString(2, user.getText());
            stmt.setString(3, password.getText());

            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
            nama.setText("");
            user.setText("");
            password.setText("");
            idpetugas.setText("");

        }catch (SQLException e) {
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_bsimpanActionPerformed

    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("update tbpetugas set nama=?, username=?, password=? where idpetugas=?");
            stmt.setString(1, nama.getText());
            stmt.setString(2, user.getText());
            stmt.setString(3, password.getText());
            stmt.setString(4, idpetugas.getText());

            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();

            nama.setText("");
            user.setText("");
            password.setText("");
            idpetugas.setText("");

            bBaru.setEnabled(true);
            bsimpan.setEnabled(true);
            bedit.setEnabled(true);
            bhapus.setEnabled(true);

        }catch (SQLException e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_beditActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        // TODO add your handling code here:
        try{
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("delete from tbpetugas where idpetugas='"+idpetugas.getText()+"'");
            stmt.executeUpdate();
            conn.close();
            TampilData();
            JOptionPane.showMessageDialog(null, "Data berhasil di hapus");
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "proses hapus gagal/koneksi gagal..");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_bhapusActionPerformed

